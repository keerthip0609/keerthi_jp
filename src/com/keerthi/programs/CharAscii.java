package com.keerthi.programs;

public class CharAscii {
	// ascii value of char h
	public static void main(String[]args) {
		char a = 'h';
		int ch = a;
		int ascii = (int) ch;
		System.out.println("ASCII value is " +ascii);
		
	// add  3 to ascii value of char 'd'
		int ans = 3 + 'd';
		System.out.println((char)ans);
	}
}
