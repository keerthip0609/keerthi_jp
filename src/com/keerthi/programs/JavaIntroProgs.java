package com.keerthi.programs;

public class JavaIntroProgs {
	public static void main(String[] args) {
		//Hello world
		System.out.println("Hello, World!");
		//variable declaration
		int speed_Limit = 100;
		System.out.println("The speed limit is: " +speed_Limit);
		
		//Literals
		//boolean literals
		boolean flag1 = false;
		boolean flag2 = true;
		System.out.println(flag1);
		System.out.println(flag2);
		//integer literals
		// binary
		int binaryNumber = 0b10010;
		System.out.println(binaryNumber);
		// octal 
		int octalNumber = 027;
		System.out.println(octalNumber);
		// decimal
		int decNumber = 34;
		System.out.println(decNumber);
		// hexadecimal 
		int hexNumber = 0x2F;
		System.out.println(hexNumber);
		// binary
		int binNumber = 0b10010;
		System.out.println(binNumber);
		
		// floating literals
		double myDouble = 3.4;
	    float myFloat = 3.4F;
	    System.out.println(myDouble);
	    System.out.println(myFloat);
	    
	    // character literals
	    char letter = 'a';
	    System.out.println("The character is: " +letter);
	    
	    //String literals
	    String str = "Keerthi Pothireddy";
	    System.out.println("The string literal is: " +str);
	    		
	    //Datatypes
	    byte b = 124;
	    int i = 127;
	    short s = 123;
	    long l = 123456789;
	    float f = 12;
	    double d = 5.5;
	    char c = 'P';
	    String st = "Java";
	    System.out.println(b);
	    System.out.println(i);
	    System.out.println(s);
	    System.out.println(l);
	    System.out.println(f);
	    System.out.println(d);
	    System.out.println(c);
	    System.out.println(st);
	    
	}
}
